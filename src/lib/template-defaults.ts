/**
 * Get template-specific sample data file path
 */
export function getTemplateSampleDataPath(templateId: string): string | null {
  const samplePaths: Record<string, string> = {
    'dj-template': '/data/dj-sample.json'
  };

  return samplePaths[templateId] || null;
}

/**
 * Load template sample data from file
 */
export async function loadTemplateSampleData(templateId: string): Promise<any> {
  try {
    const samplePath = getTemplateSampleDataPath(templateId);
    if (!samplePath) {
      return getTemplateDefaultData(templateId);
    }

    const response = await fetch(samplePath);
    if (!response.ok) {
      throw new Error(`Failed to load sample data: ${response.statusText}`);
    }

    return await response.json();
  } catch (error) {
    console.error(`Error loading sample data for ${templateId}:`, error);
    return getTemplateDefaultData(templateId);
  }
} 